package com.bbende.tripod.solr.example;

import com.bbende.tripod.api.entity.AbstractEntity;

import java.util.Date;

/**
 * Example for returning a full document, ExampleSummary would be the search result.
 *
 * @author bbende
 */
public class Example extends AbstractEntity {

    private String title;
    private String body;
    private String color;
    private Date createDate;

    public Example(String id) {
        super(ExampleField.ID, id);
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getBody() {
        return body;
    }

    public void setBody(String body) {
        this.body = body;
    }

    public String getColor() {
        return color;
    }

    public void setColor(String color) {
        this.color = color;
    }

    public Date getCreateDate() {
        return createDate;
    }

    public void setCreateDate(Date createDate) {
        this.createDate = createDate;
    }

}
